CREATE TABLE business_application (
 application_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, application_no VARCHAR(40) NOT NULL UNIQUE,
 business_type VARCHAR(50) NOT NULL,title VARCHAR(200) NOT NULL,applicant_user_id BIGINT NOT NULL,
 applicant_department_id BIGINT,applicant_region_code VARCHAR(20),target_person_id BIGINT,target_household_id BIGINT,
 status VARCHAR(30) NOT NULL,reason CLOB NOT NULL,remark VARCHAR(500),submitted_at TIMESTAMP,completed_at TIMESTAMP,
 version INT NOT NULL DEFAULT 0,created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
 FOREIGN KEY(applicant_user_id) REFERENCES sys_user(user_id),FOREIGN KEY(applicant_department_id) REFERENCES sys_department(department_id)
);
CREATE TABLE application_material (
 material_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,application_id BIGINT NOT NULL,material_type VARCHAR(50) NOT NULL,
 material_name VARCHAR(200) NOT NULL,original_filename VARCHAR(255) NOT NULL,stored_filename VARCHAR(255) NOT NULL,
 storage_path VARCHAR(500) NOT NULL,content_type VARCHAR(100) NOT NULL,file_size BIGINT NOT NULL,file_sha256 CHAR(64) NOT NULL,
 required_flag TINYINT NOT NULL,verify_status VARCHAR(30) NOT NULL,verify_user_id BIGINT,verify_comment VARCHAR(500),verified_at TIMESTAMP,
 uploaded_by BIGINT NOT NULL,created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
 FOREIGN KEY(application_id) REFERENCES business_application(application_id),FOREIGN KEY(verify_user_id) REFERENCES sys_user(user_id),FOREIGN KEY(uploaded_by) REFERENCES sys_user(user_id)
);
CREATE TABLE sys_approval_request (
