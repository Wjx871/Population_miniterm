 status VARCHAR(30) NOT NULL,current_approver_id BIGINT,current_department_id BIGINT,current_region_code VARCHAR(20),submitted_by BIGINT NOT NULL,
 submitted_at TIMESTAMP NOT NULL,decided_by BIGINT,decided_at TIMESTAMP,decision_comment VARCHAR(500),version INT NOT NULL DEFAULT 0,
